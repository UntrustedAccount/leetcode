bool confusingNumber(int n)
{
    unordered_map<int, int> mapping;
    mapping[0] = 0;
    mapping[1] = 1;
    mapping[6] = 9;
    mapping[8] = 8;
    mapping[9] = 6;

    int i = n;
    int j = 0;
    while (n > 0) {
        cout << n%10 << endl;
        if (!mapping.count(n % 10)) return false;
        j += mapping[n % 10];
        j *= 10;
        n /= 10;
    }
    j /= 10;
    return i != j;
}

