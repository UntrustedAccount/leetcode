bool areOccurrencesEqual(string s)
{
    map<char, int> mp;
    for (char &c : s)
        mp[c]++;
    int tot = mp.begin()->second;
    for (auto it = ++mp.begin(); it != mp.end(); it++)
    {
        if (it->second != tot)
            return false;
    }
    return true;
}

