int countSymmetricIntegers(int low, int high) {
    int count = 0;
    for (int i=low; i<=high; i++) {
        string s = to_string(i);
        int len = s.size();
        if (len % 2 != 0) continue;

        int l = 0, r = 0;
        for (int j=0; j<len/2; j++) {
            l += s[j] - '0';
            r += s[len - j - 1] - '0';
        }
        count += (l == r);
    }
    return count;
}

