int longestOnes(vector<int>& nums, int k) {
    int cur = 0;
    int j = 0;
    int ans = 0;
    for (int i=0; i<nums.size(); i++) {
        if (nums[i] == 0) {
            cur++;
        }
        while (cur > k) {
            if (nums[j] == 0) {
                cur--;
            }
            j++;
        }
        ans = max(ans, i - j + 1);
    }
    return ans;
}